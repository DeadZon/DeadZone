"""
Scan third_party/mezo_core/SuperConfig/ and report device status.

Usage:
    python -m factory.tools.scan_superconfig_devices
    python -m factory.tools.scan_superconfig_devices --write-doc
"""

import argparse
import sys
from pathlib import Path

from factory.core.paths import MEZO_CORE, REGISTRY_ROOT

SUPERCONFIG_ROOT = MEZO_CORE / "SuperConfig"

LIVE_STATUSES = {"golden", "ready"}


def _load_device_groups() -> dict[str, str]:
    """Return {codename: status} for all live devices across all groups."""
    try:
        import yaml
    except ImportError:
        return {}

    result: dict[str, str] = {}
    dg_dir = REGISTRY_ROOT / "device_groups"
    if not dg_dir.exists():
        return result
    for gf in dg_dir.glob("*.yml"):
        try:
            with gf.open() as f:
                data = yaml.safe_load(f)
            for entry in data.get("devices", []):
                codename = entry.get("codename")
                status = entry.get("status", "unknown")
                if codename:
                    result[codename] = status
        except Exception:
            pass
    return result


def _has_registry_file(codename: str) -> bool:
    for p in REGISTRY_ROOT.rglob(f"{codename}.yml"):
        return True
    return False


def scan() -> list[dict]:
    if not SUPERCONFIG_ROOT.exists():
        print(f"[ERROR] SuperConfig not found: {SUPERCONFIG_ROOT}", file=sys.stderr)
        return []

    live_devices = _load_device_groups()
    rows = []

    for d in sorted(SUPERCONFIG_ROOT.iterdir()):
        if not d.is_dir():
            continue
        codename = d.name
        has_parts = (d / "parts_info").exists()
        has_super = (d / "super").exists()
        has_registry = _has_registry_file(codename)
        group_status = live_devices.get(codename, "not_grouped")

        rows.append({
            "codename": codename,
            "parts_info": has_parts,
            "super": has_super,
            "registry": has_registry,
            "status": group_status,
        })

    return rows


def build_markdown(rows: list[dict]) -> str:
    lines = [
        "# Device Classification TODO",
        "",
        "Generated by `factory/tools/scan_superconfig_devices.py`.",
        "Devices scanned from `third_party/mezo_core/SuperConfig/`.",
        "",
        "## Legend",
        "",
        "| Status | Meaning |",
        "|---|---|",
        "| golden | Primary golden device — live in panel |",
        "| ready | Registry confirmed — live in panel |",
        "| todo | Needs SoC/name confirmation before adding to panel |",
        "| unknown | Not classified yet |",
        "| not_grouped | Scanned but not in any device_group file |",
        "",
        "## Scanned Devices",
        "",
        "| Codename | parts_info | super | Registry | Status | Notes |",
        "|---|:---:|:---:|:---:|---|---|",
    ]

    for r in rows:
        codename = r["codename"]
        parts = "yes" if r["parts_info"] else "no"
        sup = "yes" if r["super"] else "no"
        reg = "yes" if r["registry"] else "no"
        status = r["status"]

        if status in LIVE_STATUSES:
            notes = "Added to live panel"
        elif reg == "yes":
            notes = "Registry exists — confirm SoC and add to panel"
        else:
            notes = "SoC/name not confirmed — do not add until verified"

        lines.append(f"| {codename} | {parts} | {sup} | {reg} | {status} | {notes} |")

    total = len(rows)
    live = sum(1 for r in rows if r["status"] in LIVE_STATUSES)
    todo = total - live
    lines += [
        "",
        f"**Total scanned:** {total}  ",
        f"**Live in panels:** {live}  ",
        f"**TODO / unclassified:** {todo}  ",
        "",
        "## How to Add a Device",
        "",
        "1. Confirm SoC (Snapdragon or MTK — do not guess).",
        "2. Confirm marketing name.",
        "3. Create `registry/devices/<soc>/<codename>.yml`.",
        "4. Add entry to `registry/device_groups/<soc>.yml` with `status: ready`.",
        "5. Add `\"<codename> | <Marketing Name>\"` to the correct workflow device options.",
        "6. Test: `python -m factory.cli plan --device <codename> --soc <soc> ...`",
    ]

    return "\n".join(lines) + "\n"


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Scan SuperConfig devices and report classification status."
    )
    parser.add_argument(
        "--write-doc",
        action="store_true",
        help="Write report to docs/DEVICE_CLASSIFICATION_TODO.md",
    )
    args = parser.parse_args()

    rows = scan()
    if not rows:
        sys.exit(1)

    md = build_markdown(rows)

    if args.write_doc:
        out = REGISTRY_ROOT.parent / "docs" / "DEVICE_CLASSIFICATION_TODO.md"
        out.parent.mkdir(parents=True, exist_ok=True)
        out.write_text(md, encoding="utf-8")
        print(f"[OK] Written: {out}")
    else:
        print(md)


if __name__ == "__main__":
    main()
