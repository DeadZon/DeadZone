"""Rebuild critical text files from explicit line arrays using raw LF bytes.

Never use PowerShell Out-File, Set-Content, or editor save for these files.
Always write via Path.write_bytes to guarantee LF-only output.
"""
from __future__ import annotations

from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]


def write_lf(rel_path: str, lines: list[str]) -> None:
    path = REPO_ROOT / rel_path
    path.parent.mkdir(parents=True, exist_ok=True)
    data = ("\n".join(lines) + "\n").encode("utf-8")
    path.write_bytes(data)
    lf_count = data.count(b"\n")
    cr_count = data.count(b"\r")
    print(f"  wrote {rel_path}: {len(lines)} lines, {lf_count} LF, {cr_count} CR")


